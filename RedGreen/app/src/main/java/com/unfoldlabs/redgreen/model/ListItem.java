package com.unfoldlabs.redgreen.model;

import android.graphics.Bitmap;
import android.graphics.drawable.Drawable;

public class ListItem {

	private String historyUrl;
	private String historyTitle;
	private Bitmap historyBitmap;
	private String apptitle;
	private Drawable appIcon;
	private String cacheAppName;
	private String cacheSize;
	private Bitmap cacheAppIcon;
	private long id;
	private String info;
	private Drawable icon;
	private String packageName;

	public String getPackageName() {
		return packageName;
	}

	public void setPackageName(String packageName) {
		this.packageName = packageName;
	}

	public long getId() {
		return id;
	}

	public void setId(long id) {
		this.id = id;
	}

	public String getInfo() {
		return info;
	}

	public void setInfo(String info) {
		this.info = info;
	}

	public Drawable getIcon() {
		return icon;
	}

	public void setIcon(Drawable icon) {
		this.icon = icon;
	}

	public String getHistoryUrl() {
		return historyUrl;
	}

	public void setHistoryUrl(String historyUrl) {
		this.historyUrl = historyUrl;
	}

	public String getHistoryTitle() {
		return historyTitle;
	}

	public void setHistoryTitle(String historyTitle) {
		this.historyTitle = historyTitle;
	}

	public Bitmap getHistoryBitmap() {
		return historyBitmap;
	}

	public void setHistoryBitmap(Bitmap historyBitmap) {
		this.historyBitmap = historyBitmap;
	}

	public String getApptitle() {
		return apptitle;
	}

	public void setApptitle(String apptitle) {
		this.apptitle = apptitle;
	}

	public Drawable getAppIcon() {
		return appIcon;
	}

	public void setAppIcon(Drawable appIcon) {
		this.appIcon = appIcon;
	}

	public String getCacheAppName() {
		return cacheAppName;
	}

	public void setCacheAppName(String cacheAppName) {
		this.cacheAppName = cacheAppName;
	}

	public String getCacheSize() {
		return cacheSize;
	}

	public void setCacheSize(String cacheSize) {
		this.cacheSize = cacheSize;
	}

	public Bitmap getCacheAppIcon() {
		return cacheAppIcon;
	}

	public void setCacheAppIcon(Bitmap cacheAppIcon) {
		this.cacheAppIcon = cacheAppIcon;
	}

}
